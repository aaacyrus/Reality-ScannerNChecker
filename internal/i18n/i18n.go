package i18n

import (
	"fmt"

	"github.com/aaacyrus/Reality-ScannerNChecker/internal/domain"
)

type Translator struct {
	Language domain.Language
}

func (t Translator) T(key string, args ...any) string {
	message := english[key]
	if t.Language == domain.LanguageTraditionalChinese {
		if translated, ok := traditionalChinese[key]; ok {
			message = translated
		}
	}
	if message == "" {
		message = key
	}
	return fmt.Sprintf(message, args...)
}

func (t Translator) Reason(code string) string {
	return t.T("reason." + code)
}

